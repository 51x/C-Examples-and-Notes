#include <iostream>

using namespace std;

int main()
{
    int numx = 22, numy = 4;

    cout << numx << "+" << numy << "=" << numx+numy << endl;
    cout << numx << "-" << numy << "=" << numx-numy << endl;
    cout << numx << "*" << numy << "=" << numx*numy << endl;
    cout << numx << "/" << numy << "=" << numx/numy << endl;

    return 0;

}
