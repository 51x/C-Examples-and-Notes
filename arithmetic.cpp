#include <iostream>

int main()
{
    int numx = 22, numy = 4;
    /* \n for new lines */
    std::cout << numx << "+" << numy << "=" << numx+numy << "\n";
    std::cout << numx << "-" << numy << "=" << numx-numy << "\n";
    std::cout << numx << "*" << numy << "=" << numx*numy << "\n";
    std::cout << numx << "/" << numy << "=" << numx/numy << "\n";

    return 0;

}
